CREATE SEQUENCE ENDERECO_ID_SEQ AS BIGINT START WITH 1 INCREMENT BY 1;

CREATE TABLE ENDERECO (
	ID BIGINT GENERATED BY DEFAULT AS SEQUENCE ENDERECO_ID_SEQ,
	LOGRADOURO VARCHAR(255),
	NUMERO INT,
	COMPLEMENTO VARCHAR(255),
	BAIRRO VARCHAR(255),
	CIDADE VARCHAR(255),
	ESTADO VARCHAR(2),
	ID_PESSOA BIGINT NOT NULL,
	
	PRIMARY KEY (ID),
	FOREIGN KEY (ID_PESSOA) REFERENCES PESSOA(ID)
);